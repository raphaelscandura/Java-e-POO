package br.com.scandura;

public class Calculadora {

    public int somar(int a, int b){
        return a + b;
    }

    public double subtrair(double a, double b){
        return a - b;
    }

    public double multiplicar(double a, double b){
        return a * b;
    }

    public double dividr(double a, double b){
        return a / b;
    }
    
}