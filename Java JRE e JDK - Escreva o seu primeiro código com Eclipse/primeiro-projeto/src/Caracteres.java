
public class Caracteres {
	public static void main(String[] args) {
		
		char letra = 'J';
		
		System.out.println(letra);
		
		char valorLetra = 66;
		System.out.println(valorLetra);
		
		valorLetra = (char) (valorLetra + 5);
		System.out.println(valorLetra);
		
		String frase = "João Pedro joga pedra ";
		System.out.println(frase);
		
		frase = frase + 2050;
		System.out.println(frase);
	}
}
